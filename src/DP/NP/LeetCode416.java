    /*自己做的，错误点在哪里*/
    public boolean sum(int nums[], int pos){
        int sum1 = 0;
        for (int i = 0 ; i <= pos ;i ++){
            sum1 += nums[i];
        }
        int sum2 = 0;
        for (int i = pos + 1; i < nums.length ; i ++){
            sum2 += nums[i];
        }
        return sum1 == sum2;
    }
